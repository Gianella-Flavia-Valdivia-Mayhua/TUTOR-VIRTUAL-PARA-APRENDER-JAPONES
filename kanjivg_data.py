def get_strokes(char):
    """Retorna los strokes y colores para un carácter dado."""
    data = (HIRAGANA_STROKES.get(char) or
            KATAKANA_STROKES.get(char) or
            KANJI_STROKES.get(char))
    if not data:
        return None
    strokes = data["strokes"]
    colors  = [STROKE_COLORS[i % len(STROKE_COLORS)] for i in range(len(strokes))]
    result  = {
        "strokes": strokes,
        "colors": colors,
        "points": [svg_path_to_points(path) for path in strokes],
        "viewBox": [0, 0, 109, 109],
    }
    if "reading" in data:
        result["reading"] = data["reading"]
        result["meaning"] = data["meaning"]
    return result
